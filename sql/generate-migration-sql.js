#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * generate-migration-sql.js
 *
 * Reads an existing tasks JSON file (the one the standalone HTML used to
 * persist in localStorage / download) and emits a .sql script you can run
 * against your Azure SQL database with sqlcmd, Azure Data Studio, or SSMS.
 *
 * Usage:
 *   node sql/generate-migration-sql.js path/to/tasks.json > sql/03-import.sql
 *
 * Input shape (either is accepted):
 *   { "tasks": [ { person, task, start, end, capacityImpact, complete, milestones } ] }
 *   [ { person, task, ... } ]
 *
 * No dependencies — runs on plain Node 18+.
 */

const fs = require('fs');
const path = require('path');

const inputPath = process.argv[2];
if (!inputPath) {
    console.error('Usage: node sql/generate-migration-sql.js <tasks.json>');
    process.exit(2);
}

const raw = fs.readFileSync(path.resolve(inputPath), 'utf8');
let parsed;
try { parsed = JSON.parse(raw); }
catch (e) { console.error('Input is not valid JSON:', e.message); process.exit(2); }

const tasks = Array.isArray(parsed) ? parsed
            : (parsed && Array.isArray(parsed.tasks)) ? parsed.tasks
            : null;
if (!tasks) {
    console.error('Input must be a JSON array or an object with a "tasks" array.');
    process.exit(2);
}

// SQL string literal escaping for nvarchar: double single-quotes.
const sqlStr   = (s) => `N'${String(s).replace(/'/g, "''")}'`;
const sqlDate  = (s) => (s && String(s).trim()) ? `'${String(s).slice(0,10)}'` : 'NULL';
const sqlBit   = (b) => b ? '1' : '0';
const sqlImpact = (v) => {
    const x = (v || 'medium').toLowerCase();
    return (x === 'high' || x === 'medium' || x === 'low') ? sqlStr(x) : sqlStr('medium');
};

const lines = [];
lines.push('-- Auto-generated by sql/generate-migration-sql.js');
lines.push(`-- Source: ${path.resolve(inputPath)}`);
lines.push(`-- Generated: ${new Date().toISOString()}`);
lines.push(`-- Rows: ${tasks.length}`);
lines.push('');
lines.push('SET XACT_ABORT ON;');
lines.push('BEGIN TRAN;');
lines.push('');
lines.push('-- Uncomment the next line to fully replace existing data:');
lines.push('-- TRUNCATE TABLE gantt.Tasks;');
lines.push('');

// Batch into INSERT...VALUES groups of ~500 rows (T-SQL caps at 1000 per statement).
const BATCH = 500;
for (let i = 0; i < tasks.length; i += BATCH) {
    const slice = tasks.slice(i, i + BATCH);
    lines.push('INSERT INTO gantt.Tasks (Person, TaskName, StartDate, EndDate, CapacityImpact, Complete, MilestonesJson)');
    lines.push('VALUES');
    slice.forEach((t, idx) => {
        const milestones = (t && typeof t.milestones === 'object' && !Array.isArray(t.milestones))
            ? t.milestones
            : {};
        const tuple = [
            sqlStr(t.person || ''),
            sqlStr(t.task || ''),
            sqlDate(t.start),
            sqlDate(t.end),
            sqlImpact(t.capacityImpact),
            sqlBit(!!t.complete),
            sqlStr(JSON.stringify(milestones)),
        ].join(', ');
        const sep = idx === slice.length - 1 ? ';' : ',';
        lines.push(`    (${tuple})${sep}`);
    });
    lines.push('');
}

lines.push('COMMIT TRAN;');
lines.push(`PRINT 'Imported ${tasks.length} task(s).';`);
lines.push('');

process.stdout.write(lines.join('\n'));
